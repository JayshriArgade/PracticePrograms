
public class NumericDataType {

	public static void main(String[] args) 
	{
		
		System.out.println("----------------------- BYTE DATA TYPE -------------------------");
	   // byte - Primitive data type
	   byte a=120;
	   byte b=12;
	   int add1 = a+b;
	   System.out.println("Addition of 2 byte numbers= "+add1);
	   int sub1 = a-b;
	   System.out.println("Subtraction of 2 byte numbers= "+sub1);
	   int mult1 = a*b;
	   System.out.println("Multiplication of 2 byte numbers= "+mult1);
	   int div1 = a/b;
	   System.out.println("Division of 2 byte numbers= "+div1);
	   int mod1 = a%b;
	   System.out.println("Mod of 2 byte numbers= "+mod1);
	   
	   System.out.println("----------------------- SHORT DATA TYPE -------------------------");
	   
	   // short - Primitive data type
	   short c=148;
	   short d=56;
	   int add2 = c+d;
	   System.out.println("Addition of 2 short numbers= "+add2);
	   int sub2 = c-d;
	   System.out.println("Subtraction of 2 short numbers= "+sub2);
	   int mult2 = c*d;
	   System.out.println("Multiplication of 2 short numbers= "+mult2);
	   int div2 = c/d;
	   System.out.println("Division of 2 short numbers= "+div2);
	   int mod2 = c%d;
	   System.out.println("Mod of 2 short numbers= "+mod2);
	   
	   System.out.println("-------------------- INT DATA TYPE ----------------------------");
	   
	   // int - Primitive data type
	   int e=12345;
	   int f=5678;
	   int add3 = e+f;
	   System.out.println("Addition of 2 int numbers= "+add3);
	   int sub3 = e-f;
	   System.out.println("Subtraction of 2 int numbers= "+sub3);
	   int mult3 = e*f;
	   System.out.println("Multiplication of 2 int numbers= "+mult3);
	   int div3 = e/f;
	   System.out.println("Division of 2 int numbers= "+div3);
	   int mod3 = e%f;
	   System.out.println("Mod of 2 int numbers= "+mod3);
	   
	   System.out.println("-------------------- LONG DATA TYPE ----------------------------");
	   // long - Primitive data type
	   long g=1484564785l;
	   long h=56857495l;
	   long add4 = g+h;
	   System.out.println("Addition of 2 long numbers= "+add4);
	   long sub4 = g-h;
	   System.out.println("Subtraction of 2 long numbers= "+sub4);
	   long mult4 = g*h;
	   System.out.println("Multiplication of 2 long numbers= "+mult4);
	   long div4 = g/h;
	   System.out.println("Division of 2 long numbers= "+div4);
	   long mod4 = g%h;
	   System.out.println("Mod of 2 long numbers= "+mod4);
	}

}
